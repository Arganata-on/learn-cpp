#include <iostream>
using namespace std;

int main() {
  // integer (whole number)
  int age = 21;
  int year = 2023;
  int days = 7;

  // double (number including decimal)
  double price = 10.99;
  double gpa = 2.5;
  double temperature = 25.1;

  // single character
  char grade = 'A';
  char initial = 'C';
  char dollarSign = '$';

  // boolean (true of false)
  bool student = false;
  bool power = true;
  bool forSale = true;

  // string (object that represents a sequence of text)
  string name = "Arga";
  string day = "Friday";
  string food = "Pizza";
  string address = "123 Fake st.";

  cout << "Hello " << name << '\n';
  cout << "You are " << age << " years old";

  return 0;
}