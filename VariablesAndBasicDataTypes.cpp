#include <iostream>
using namespace std;

int main()
{
  // integer (bilangan bulat)
  int umur = 21;
  int tahun = 2023;
  int minggu = 7;

  // double (bilangan desimal)
  double harga = 10.99;
  double gpa = 2.5;
  double suhu = 25.1;

  // char (karakter tunggal)
  char nilai = 'S';
  char inisial = 'A';
  char tandaDollar = '$'; // tanda dolar

  // boolean (benar atau salah)
  bool student = false;
  bool power = true;
  bool forSale = true;

  // string (objek yang mewakili urutan teks)
  string nama = "Arga";
  string hari = "Jumat";
  string makanan = "Pizza";
  string alamat = "123 Fake st.";

  // Mencetak teks ke konsol
  cout << "Halo " << nama << '\n';
  cout << "Kamu berumur " << umur << " tahun";

  return 0;
}
