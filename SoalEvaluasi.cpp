/*Tulis program yang meminta pengguna untuk memasukkan sebuah kata dan kemudian menentukan apakah kata tersebut palindrom
atau tidak.*/

#include <iostream>
#include <string>
using namespace std;

int main()
{
    string kata, simpanKata;

    cout << "Masukkan sebuah kata: ";
    getline(cin, kata);

    kata == simpanKata;

    cout << kata;

    return 0;
}